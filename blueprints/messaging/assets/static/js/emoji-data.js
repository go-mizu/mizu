// Recent emoji management
const RECENT_EMOJI_KEY = 'mizu_recent_emoji';
const MAX_RECENT = 24;

function getRecentEmoji() {
    try {
        const stored = localStorage.getItem(RECENT_EMOJI_KEY);
        return stored ? JSON.parse(stored) : [];
    } catch {
        return [];
    }
}

function addRecentEmoji(emoji) {
    let recent = getRecentEmoji();
    // Remove if already exists
    recent = recent.filter(e => e !== emoji);
    // Add to front
    recent.unshift(emoji);
    // Limit size
    recent = recent.slice(0, MAX_RECENT);
    localStorage.setItem(RECENT_EMOJI_KEY, JSON.stringify(recent));
    return recent;
}

