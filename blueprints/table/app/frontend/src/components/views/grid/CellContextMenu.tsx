export function CellContextMenu({
  x,
  y,
  recordId,
  fieldId,
  onClose,
  onAction,
}: CellContextMenuProps) {
  const menuRef = useRef<HTMLDivElement>(null);

  // Close on click outside
  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (menuRef.current && !menuRef.current.contains(e.target as Node)) {
        onClose();
      }
    };

    const handleKeyDown = (e: KeyboardEvent) => {
      if (e.key === 'Escape') {
        onClose();
      }
    };

    document.addEventListener('mousedown', handleClickOutside);
    document.addEventListener('keydown', handleKeyDown);
    return () => {
      document.removeEventListener('mousedown', handleClickOutside);
      document.removeEventListener('keydown', handleKeyDown);
    };
  }, [onClose]);

  // Adjust position to stay in viewport
  useEffect(() => {
    if (menuRef.current) {
      const rect = menuRef.current.getBoundingClientRect();
      const viewportWidth = window.innerWidth;
      const viewportHeight = window.innerHeight;

      let adjustedX = x;
      let adjustedY = y;

      if (x + rect.width > viewportWidth) {
        adjustedX = viewportWidth - rect.width - 10;
      }

      if (y + rect.height > viewportHeight) {
        adjustedY = viewportHeight - rect.height - 10;
      }

      menuRef.current.style.left = `${adjustedX}px`;
      menuRef.current.style.top = `${adjustedY}px`;
    }
  }, [x, y]);

  const handleItemClick = (item: MenuItem) => {
    if (item.divider) return;
    onAction(item.action, recordId, fieldId);
    onClose();
  };

  return (
    <>
      {/* Backdrop */}
      <div className="fixed inset-0 z-40" onClick={onClose} />

      {/* Menu */}
      <div
        ref={menuRef}
        className="fixed z-50 bg-white rounded-lg shadow-xl border border-slate-200 py-1 min-w-[200px] animate-slide-in"
        style={{ left: x, top: y }}
      >
        {menuItems.map((item, index) =>
          item.divider ? (
            <hr key={index} className="my-1 border-slate-200" />
          ) : (
            <button
              key={item.action}
              onClick={() => handleItemClick(item)}
              className={`w-full px-3 py-2 flex items-center gap-3 text-left text-sm hover:bg-slate-100 ${
                item.danger ? 'text-red-600 hover:bg-red-50' : 'text-slate-700'
              }`}
            >
              <span className={item.danger ? 'text-red-500' : 'text-slate-400'}>
                {item.icon}
              </span>
              <span className="flex-1">{item.label}</span>
              {item.shortcut && (
                <span className="text-xs text-slate-400">{item.shortcut}</span>
              )}
            </button>
          )
        )}
      </div>
    </>
  );
}
